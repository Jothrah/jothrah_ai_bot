def normalize_restricted_text(text):
    if not text:
        return ""

    text = str(text).strip().lower()

    replacements = {
        "أ": "ا",
        "إ": "ا",
        "آ": "ا",
        "ة": "ه",
        "ى": "ي",
        "ؤ": "و",
        "ئ": "ي",
        "-": " ",
        "_": " ",
        "(": " ",
        ")": " ",
    }

    for old, new in replacements.items():
        text = text.replace(old, new)

    return " ".join(text.split())


def find_restricted_pesticide(text):
    if not text:
        return None, None

    query = normalize_restricted_text(text)

    for name, data in RESTRICTED_DATABASE.items():
        candidates = [name, data.get("arabic", "")]
        candidates.extend(data.get("aliases", []))

        for candidate in candidates:
            candidate_norm = normalize_restricted_text(candidate)

            if query == candidate_norm or candidate_norm == query:
                return name, data

    return None, None
